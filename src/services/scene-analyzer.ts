/**
 * Scene Analyzer Service
 *
 * Uses Gemini 2.5 Flash to forensically segment a video into individual scenes
 * with precise timestamps, scene types, and detailed descriptions. This is the
 * foundation for video cloning with product swaps — it identifies:
 *
 * - TALKING_HEAD scenes (person speaking to camera)
 * - BROLL scenes (cutaway footage, transitions, establishing shots)
 * - PRODUCT_SHOT scenes (product close-ups, demos, unboxing)
 * - TRANSITION scenes (motion graphics, text cards, wipes)
 *
 * Each scene includes enough detail for downstream services to regenerate it
 * with different products/avatars while preserving the original video's DNA.
 */

import { GoogleGenAI } from "@google/genai";
import { getGeminiKey } from "../lib/keys";
import { parseLlmJson } from "../lib/parse-llm-json";
import { resolveVideoUrl } from "./video-url-resolver";
import * as fs from "fs";
import * as path from "path";

// ─────────────────────────────────────────────
// Types
// ─────────────────────────────────────────────

export type SceneType = "talking_head" | "broll" | "product_shot" | "transition";

export interface DetectedProduct {
  name: string;
  description: string;
  position: string; // e.g., "held in right hand", "on counter left side"
  visibleFeatures: string; // color, shape, packaging type
  interactionType: string; // "held", "placed", "being applied", "opened", etc.
}

export interface SceneSegment {
  index: number;
  type: SceneType;
  startSeconds: number;
  endSeconds: number;
  durationSeconds: number;
  description: string; // Dense visual description of the scene
  cameraWork: string; // Camera angle, movement, shot type
  lighting: string; // Lighting conditions
  environment: string; // Setting/background
  subject: string; // Who/what is in the scene
  dialogue: string; // Spoken words in this segment (empty for B-roll)
  products: DetectedProduct[]; // Products visible in this scene
  motionDescription: string; // Physical movements/actions happening
  colorPalette: string; // Dominant colors
  energy: string; // low/medium/high
  isSwappable: boolean; // Can this scene's product be swapped?
  swapDifficulty: "easy" | "medium" | "hard"; // How hard to swap the product
  swapNotes: string; // Guidance for product swap
}

export interface SceneAnalysisResult {
  totalDuration: number;
  aspectRatio: "9:16" | "16:9" | "1:1";
  sceneCount: number;
  scenes: SceneSegment[];
  overallStyle: string; // Summary of the video's visual DNA
  speakerProfile: string; // Description of the main speaker
  productSummary: string; // Summary of all products detected
  scriptFull: string; // Full transcription
  hookStrategy: string; // How the video grabs attention
  targetPlatform: string; // TikTok, Instagram, YouTube Shorts
  resolvedVideoUrl?: string; // Direct download URL (set when social media URL is resolved)
}

