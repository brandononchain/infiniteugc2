/**
 * Analyze a video and segment it into individual scenes with timestamps,
 * types, descriptions, product detection, and swap guidance.
 *
 * Uses Gemini 2.5 Flash's video understanding capabilities to produce
 * a structured JSON breakdown of every scene in the video.
 */
export async function analyzeVideoScenes(videoUrl: string): Promise<SceneAnalysisResult> {
  let tempPath: string | null = null;

  try {
    const geminiKey = await getGeminiKey();
    const ai = new GoogleGenAI({ apiKey: geminiKey });

    // Resolve social media URLs to direct download links
    const resolvedUrl = await resolveVideoUrl(videoUrl);

    // Download video to temp file
    tempPath = await downloadToTemp(resolvedUrl);

    // Upload to Gemini Files API
    console.log("[SceneAnalyzer] Uploading video to Gemini Files API...");
    const uploadResult = await ai.files.upload({
      file: tempPath,
      config: { mimeType: "video/mp4" },
    });

    if (!uploadResult.name) {
      throw new Error("Gemini file upload returned no file name");
    }

    console.log(
      `[SceneAnalyzer] File uploaded: ${uploadResult.name}, state: ${uploadResult.state}`
    );

    // Poll until ACTIVE
    let file = uploadResult;
    let pollAttempts = 0;
    const maxPollAttempts = 60;

    while (file.state === "PROCESSING" && pollAttempts < maxPollAttempts) {
      await new Promise((r) => setTimeout(r, 5000));
      pollAttempts++;
      file = await ai.files.get({ name: file.name! });
      if (pollAttempts % 5 === 0) {
        console.log(`[SceneAnalyzer] Poll ${pollAttempts}: state=${file.state}`);
      }
    }

    if (file.state !== "ACTIVE") {
      throw new Error(`Gemini file processing failed: state=${file.state}`);
    }

    // Analyze with Gemini 2.5 Flash (JSON response mode forces valid JSON output)
    console.log("[SceneAnalyzer] Sending to Gemini 2.5 Flash for scene segmentation...");
    const response = await ai.models.generateContent({
      model: "gemini-2.5-flash",
      contents: [
        {
          role: "user",
          parts: [
            {
              fileData: {
                fileUri: file.uri!,
                mimeType: "video/mp4",
              },
            },
            { text: SCENE_ANALYSIS_PROMPT },
          ],
        },
      ],
      config: {
        responseMimeType: "application/json",
      },
    });

    const rawText = response.text?.trim();

    if (!rawText) {
      throw new Error("Gemini returned empty scene analysis");
    }

    console.log(`[SceneAnalyzer] Raw response: ${rawText.length} chars`);

    // Parse JSON from Gemini response (handles code fences, trailing commas, etc.)
    let parsed: SceneAnalysisResult;
    try {
      parsed = parseLlmJson<SceneAnalysisResult>(rawText);
    } catch (parseErr: any) {
      console.error(`[SceneAnalyzer] Raw Gemini response (first 500 chars): ${rawText.substring(0, 500)}`);
      throw new Error(`Failed to parse scene analysis JSON: ${parseErr.message}`);
    }

    // Validate and sanitize the result
    if (!parsed.scenes || !Array.isArray(parsed.scenes)) {
      throw new Error("Scene analysis missing scenes array");
    }

    // Ensure indices are sequential
    parsed.scenes.forEach((scene, i) => {
      scene.index = i;
    });

    parsed.sceneCount = parsed.scenes.length;

    // Attach the resolved URL so callers can reuse it without re-resolving
    parsed.resolvedVideoUrl = resolvedUrl;

    console.log(
      `[SceneAnalyzer] Analysis complete: ${parsed.sceneCount} scenes, ` +
        `${parsed.totalDuration}s total, ${parsed.aspectRatio}`
    );

    // Log scene breakdown
    for (const scene of parsed.scenes) {
      const productCount = scene.products?.length || 0;
      console.log(
        `[SceneAnalyzer]   Scene ${scene.index}: ${scene.type} (${scene.startSeconds}s-${scene.endSeconds}s, ${scene.durationSeconds}s)` +
          ` products=${productCount} swappable=${scene.isSwappable}`
      );
    }

    // Clean up Gemini file
    try {
      await ai.files.delete({ name: file.name! });
    } catch {
      // Non-fatal
    }

    return parsed;
  } catch (error: any) {
    console.error(`[SceneAnalyzer] Failed to analyze video scenes: ${error.message}`);
    throw error;
  } finally {
    if (tempPath) {
      try {
        if (fs.existsSync(tempPath)) fs.unlinkSync(tempPath);
      } catch {
        // Non-fatal
      }
    }
  }
}

/**
 * Extract just the scenes that contain products (for swap targeting).
 */
export function getSwappableScenes(analysis: SceneAnalysisResult): SceneSegment[] {
  return analysis.scenes.filter((s) => s.isSwappable);
}

/**
 * Extract talking head scenes (for avatar replacement via Kling MC + Sync.so).
 */
export function getTalkingHeadScenes(analysis: SceneAnalysisResult): SceneSegment[] {
  return analysis.scenes.filter((s) => s.type === "talking_head");
}

/**
 * Extract B-roll scenes (for replacement with user's product footage).
 */
export function getBRollScenes(analysis: SceneAnalysisResult): SceneSegment[] {
  return analysis.scenes.filter((s) => s.type === "broll");
}

/**
 * Extract product shot scenes (highest priority for product swap).
 */
export function getProductShotScenes(analysis: SceneAnalysisResult): SceneSegment[] {
  return analysis.scenes.filter((s) => s.type === "product_shot");
}
